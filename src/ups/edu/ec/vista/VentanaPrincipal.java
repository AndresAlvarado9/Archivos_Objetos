    public void listarDatos() {
        dtmPersona.setRowCount(0);
        for (Persona persona : controladorPersona.getPersonalist()) {
            String datos[] = {String.valueOf(persona.getId()), persona.getCedula(), persona.getNombre(), persona.getApellido()};
            dtmPersona.addRow(datos);
        }
    }

    public void cargarDatos(int posicion) {
        if (posicion >= 0) {
            Persona persona = controladorPersona.getPersonalist().get(posicion);
            txtCedula.setText(persona.getCedula());
            txtNombre.setText(persona.getNombre());
            txtApellido.setText(persona.getApellido());
        }
    }
    public void limpiar() {
        txtCedula.setText("");
        txtApellido.setText("");
        txtNombre.setText("");
    }

