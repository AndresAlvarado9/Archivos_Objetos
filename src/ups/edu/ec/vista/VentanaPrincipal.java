    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        boolean resultado = false;
        if(controladorPersona.buscar(txtCedula.getText())!= null) {
            resultado = controladorPersona.actualizar(txtCedula.getText(), txtNombre.getText(), txtApellido.getText());
        }else {
            resultado = controladorPersona.crear(txtCedula.getText(), txtNombre.getText(), txtApellido.getText());
        }
        limpiar();
        listarDatos();
        JOptionPane.showMessageDialog(this, "Operación : " + resultado);
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        boolean resultado = controladorPersona.eliminar(txtCedula.getText());
        JOptionPane.showMessageDialog(this, "Operación : " + resultado);
        limpiar();
        listarDatos();
    }//GEN-LAST:event_btnEliminarActionPerformed

